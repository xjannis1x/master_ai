s="Alle meine Entchen schwimmen auf dem See."

s_lower=s.lower()

b_a=s_lower.count("a")
b_e=s_lower.count("e")
b_i=s_lower.count("i")
b_o=s_lower.count("o")
b_u=s_lower.count("u")

print("a: ",b_a)
print("e: ",b_e)
print("i: ",b_i)
print("o: ",b_o)
print("u: ",b_u)